package com.sscsweb.owar.entities;

public class TbRegioni {

	private String nome_regione;
	private String capoluogo_regione;
	private String codiceistat_regione;
	
	public String getNome_regione() {
		return nome_regione;
	}
	
	public void setNome_regione(String nome_regione) {
		this.nome_regione = nome_regione;
	}

	public String getCapoluogo_regione() {
		return capoluogo_regione;
	}

	public void setCapoluogo_regione(String capoluogo_regione) {
		this.capoluogo_regione = capoluogo_regione;
	}

	public String getCodiceistat_regione() {
		return codiceistat_regione;
	}

	public void setCodiceistat_regione(String codiceistat_regione) {
		this.codiceistat_regione = codiceistat_regione;
	}
	
}
